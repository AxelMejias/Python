# 13)  Pedir el ingreso de dos cadenas por por teclado, e 
# indicar si la segunda cadena se encuentra dentro de la primera.


cadena1 = input("Ingresá la primera cadena: ")
cadena2 = input("Ingresá la segunda cadena: ")

if cadena2 in cadena1:
    print("La segunda cadena se encuentra dentro de la primera.")
else:
    print("La segunda cadena no se encuentra dentro de la primera.")